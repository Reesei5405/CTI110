# Set up the turtle
import turtle
from turtle import*
wn =turtle.Screen()
wn.bgcolor("black")
t = turtle.Turtle()


# add some display options
t.pensize(5)
t.pencolor("purple")
t.shape("turtle")



t.forward(60)
t.right(90)
t.forward(60)
t.right(90)

t.forward(60)
t.right(90)

t.forward(60)
t.right(90)

t.pencolor("blue")
penup()
t.left(90)
t.forward(60)
t.right(130)
t.forward(80)
pendown()
